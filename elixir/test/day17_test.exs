defmodule Day17Test do
  use ExUnit.Case
  doctest Day17

  test "problem1" do
    input = """
    2413432311323
    3215453535623
    3255245654254
    3446585845452
    4546657867536
    1438598798454
    4457876987766
    3637877979653
    4654967986887
    4564679986453
    1224686865563
    2546548887735
    4322674655533
    """
    assert Day17.problem1(input, :io) == 102
  end

  test "problem2" do
    input = """
    2413432311323
    3215453535623
    3255245654254
    3446585845452
    4546657867536
    1438598798454
    4457876987766
    3637877979653
    4654967986887
    4564679986453
    1224686865563
    2546548887735
    4322674655533
    """
    assert Day17.problem2(input, :io) == 94

    input2 = """
    111111111111
    999999999991
    999999999991
    999999999991
    999999999991
    """
    assert Day17.problem2(input2, :io) == 71
  end
end
